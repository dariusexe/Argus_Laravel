    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $date = new Carbon('now');
        setlocale(LC_TIME, 'pt_BR', 'pt_BR.utf-8', 'pt_BR.utf-8', 'portuguese');
        date_default_timezone_set('America/Sao_Paulo');

        $user_log = Auth::user()->unit_id;

        $course_releases = DB::table('course_releases')
            ->join('courses', 'course_releases.course_id', '=', 'courses.id')
            ->where('course_releases.status_release_id', '=', 1)
            ->where('courses.unit_id', '=', $user_log)
            ->get()->toArray();

        return view('admin.dashboard.index', compact('date', 'course_releases'));
    }
